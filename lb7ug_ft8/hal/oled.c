static void oled_cmd(uint8_t c) {
    uint8_t b[2] = {0x00, c};
    i2c_write(SSD1306_ADDR, b, 2);
}

void oled_init(void) {
    sleep_ms(100);
    static const uint8_t seq[] = {
        0xAE, 0xD5,0x80, 0xA8,0x3F, 0xD3,0x00, 0x40,
        0x8D,0x14, 0x20,0x00, 0xA1, 0xC8, 0xDA,0x12,
        0x81,0xCF, 0xD9,0xF1, 0xDB,0x40, 0xA4, 0xA6, 0xAF
    };
    for (unsigned i = 0; i < sizeof(seq); i++) oled_cmd(seq[i]);
    memset(fb, 0, sizeof(fb));
    dirty = true;
}

void oled_clear(void) { memset(fb, 0, sizeof(fb)); dirty = true; }

void oled_flush(void) {
    if (!dirty) return;
    oled_cmd(0x21); oled_cmd(0); oled_cmd(127);
    oled_cmd(0x22); oled_cmd(0); oled_cmd(7);
    for (int p = 0; p < OLED_PAGES; p++) {
        uint8_t b[OLED_W + 1];
        b[0] = 0x40;
        memcpy(b + 1, fb[p], OLED_W);
        i2c_write(SSD1306_ADDR, b, OLED_W + 1);
    }
    dirty = false;
}

void oled_char(int x, int p, char c, bool inv) {
    if (c < 0x20 || c > 0x7E) c = ' ';
    const uint8_t *g = font[c - 0x20];
    for (int i = 0; i < 5; i++) {
        if (x + i >= OLED_W) break;
        fb[p][x+i] = inv ? ~g[i] : g[i];
    }
    if (x + 5 < OLED_W) fb[p][x+5] = inv ? 0xFF : 0x00;
    dirty = true;
}

void oled_str(int cx, int p, const char *s, bool inv) {
    int x = cx * 6;
    while (*s) { oled_char(x, p, *s++, inv); x += 6; }
}

void oled_printf(int cx, int p, bool inv, const char *fmt, ...) {
    char buf[22];
    va_list a; va_start(a, fmt);
    vsnprintf(buf, sizeof(buf), fmt, a);
    va_end(a);
    oled_str(cx, p, buf, inv);
}

void oled_hline(int x0, int x1, int p) {
    for (int x = x0; x <= x1 && x < OLED_W; x++) fb[p][x] = 0xFF;
    dirty = true;
}

void oled_smeter(int x0, int x1, int p, int8_t rssi) {
    int range = x1 - x0;
    int fill  = (rssi + 127) * range / 127;
    if (fill < 0) fill = 0;
    if (fill > range) fill = range;
    for (int x = x0; x < x1 && x < OLED_W; x++)
        fb[p][x] = (x - x0 < fill) ? 0x3C : 0x08;
    dirty = true;
}
